#include <iostream>
using namespace std;
int main()
{
    char c;
    short s;
    int i;
    long L;
    float f;
    double d;

    cout<<"No. of bytes allocated to char Datatype ="<<sizeof(c);
    cout<<"\nNo. of bytes allocated to short Datatype ="<<sizeof(s);
    cout<<"\nNo. of bytes allocated to int Datatype ="<<sizeof(i);
    cout<<"\nNo. of bytes allocated to long Datatype ="<<sizeof(L);
    cout<<"\nNo. of bytes allocated to float Datatype ="<<sizeof(f);
    cout<<"\nNo. of bytes allocated to double Datatype ="<<sizeof(d);
}
